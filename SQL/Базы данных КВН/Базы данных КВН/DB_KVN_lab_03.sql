	CREATE TABLE [dbo].[CITY] (
		city_id SMALLINT IDENTITY(101,1) NOT NULL PRIMARY KEY,
		city_name VARCHAR(15) );

	CREATE TABLE UNIVERSITY (
		university_id SMALLINT IDENTITY(201,1) NOT NULL PRIMARY KEY,
		university_name VARCHAR(40),
		city_id SMALLINT REFERENCES CITY(city_id) ON UPDATE CASCADE
		);

	CREATE TABLE TEAM (
		team_id SMALLINT IDENTITY(401,1) NOT NULL PRIMARY KEY,
		team_name VARCHAR(20),
		city_id SMALLINT REFERENCES CITY(city_id) ON UPDATE CASCADE
		);

	CREATE TABLE PEOPLE (
		person_id SMALLINT IDENTITY(301,1) NOT NULL PRIMARY KEY,
		first_name VARCHAR(30),
		last_name VARCHAR(30),
		university_id SMALLINT REFERENCES UNIVERSITY(university_id) ON UPDATE CASCADE
		);

	CREATE TABLE PLAYER (
		person_id SMALLINT REFERENCES PEOPLE(person_id),
		team_id SMALLINT REFERENCES TEAM(team_id) ON DELETE CASCADE ON UPDATE CASCADE,
		first_date DATETIME NOT NULL,
		last_date DATETIME,
		PRIMARY KEY ( person_id, team_id, first_date)
		);

	CREATE TABLE LEAGUE (
		league_id SMALLINT IDENTITY(501,1) NOT NULL PRIMARY KEY,
		league_name VARCHAR(15) );

	CREATE TABLE GAME (
		game_id SMALLINT IDENTITY(701,1) NOT NULL PRIMARY KEY,
		city_id SMALLINT REFERENCES CITY(city_id) ON UPDATE CASCADE,
		league_id SMALLINT REFERENCES LEAGUE(league_id),
		[date] DATETIME,
		game_name VARCHAR(15),
		game_stage VARCHAR(15) );

	CREATE TABLE COMPETITION (
		comp_id SMALLINT IDENTITY(601,1) NOT NULL PRIMARY KEY,
		comp_name VARCHAR(20) );

	CREATE TABLE MARKS (
		team_id SMALLINT REFERENCES Team(team_id) ON DELETE CASCADE ON UPDATE CASCADE,
		game_id SMALLINT REFERENCES Game(game_id) ON DELETE CASCADE,
		comp_id SMALLINT REFERENCES COMPETITION(comp_id),
		marks NUMERIC(1,0),
		jury_id SMALLINT REFERENCES PEOPLE(person_id),
		PRIMARY KEY (team_id, game_id, comp_id, jury_id) );

